// Arithmentic Operators
let a = 8;
let b = 7;

console.log("a = ", a, "& b = ", b); 
console.log("a = ", a, ", b = ", b);
//
console.log("a + b = ", a + b); // perform addition operator
console.log("a - b = ", a - b); // perform subtraction operator
console.log("a * b = ", a * b); // perform multiplication operator
console.log("a / b = ", a / b); // perform division operator
console.log("a % b = ", a % b); // perform modulus operator
console.log("a ** b = ", a ** b); // perform exponentiation operator